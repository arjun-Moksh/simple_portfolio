a = 1
b = 2.8

sum_ab = a + b
print(sum_ab) #output: 3.8

sub_ab = a - b
print(sub_ab) #output: -1.8

mult_ab = a * b
print(mult_ab) #output: 2.8

div_ab = a / b
print(div_ab) #output: 0.5

mod_ab = a % b
print(mod_ab) #output: 1

floor_div_ab = a // b
print(floor_div_ab) #output: 0

exp_ab = a ** b
print(exp_ab) #output: 1

